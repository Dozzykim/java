package Day03;

public class Ex12_For {

	public static void main(String[] args) {
		// for문

		// 1.
		for (int i = 1; i <= 10; i++) {
			System.out.print(i + " ");
		}
		System.out.println();

		// 2.
		for (int i = 50; i <= 100; i++) {
			System.out.print(i + " ");
		}
		System.out.println();

		// 3. 짝수출력
		for (int i = 2; i <= 20; i += 2) {
			System.out.print(i + " ");
		}
		System.out.println();

		// 4.
		for (int i = 1; i <= 20; i += 2) {
			System.out.print(i + " ");
		}
		System.out.println();

	}
}